def find_rk_v5(query):
    q = normalize_name(query)
    if not q: return None

    # 1. Exact normalized match
    if q in lookup:
        i = lookup[q]
        return {**i, 'method': 'exact'}

    # 2. Word-boundary substring on normalized
    pat = r"\b" + re.escape(q) + r"\b"
    bm = master[master['EMPLOYER_NORM'].str.contains(pat, na=False, regex=True)]
    if not bm.empty:
        top = bm.sort_values(['_tier_rank','_n'], ascending=[True, False]).iloc[0]
        return {**top.to_dict(), 'method': 'word_boundary'}

    # 3. Space-collapse fallback
    qc = collapsed(q)
    if qc != q and len(qc) >= 4:
        cm = master[master['EMPLOYER_NORM'].apply(
            lambda s: qc in collapsed(s) if pd.notna(s) else False)]
        if not cm.empty:
            top = cm.sort_values(['_tier_rank','_n'], ascending=[True, False]).iloc[0]
            return {**top.to_dict(), 'method': 'space_collapse'}

    # 4. Fuzzy fallback (last resort, threshold 92)
    try:
        match = process.extractOne(q, all_norms, scorer=fuzz.WRatio)
        if match and match[1] >= 92:
            i = lookup[match[0]]
            return {**i, 'method': f'fuzzy_{match[1]}'}
    except Exception: pass

    return None

